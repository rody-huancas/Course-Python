# PARAMETROS Y ARGUMENTOS

# Definir la función y pasarle un parámetro
def saludar(nombre):
    return f'Hola {nombre}'


# En la función le pasamos el argumento
valor = saludar('Rody Huancas')
print(valor)


# Sumar 2 números
def sumar(a, b):
    return a + b


suma = sumar(10, 8)
print(f'La suma es {suma}')
