# Definir variables: no es necesario definir el tipo de la variable
numero = 10
print(numero)

# Definir una variable vacia
miVariable = None
print(miVariable)

# Podemos definir variables con el mismo nombre, pero con minúsculas o mayúsculas, y serán variables completamente diferentes
hola = "Saludo"
Hola = "Otro Saludo"

print(hola)
print(Hola)

# Verificar un tipo de dato de la variable
print(type("Hola"))  # Esto arrojará que es un "srt => string"
print(type(23))  # tipo de dato "int => entero"
print(type(45.33))  # tipo de dato "float => decimal"
