# IF - ELSE

# Si es verdadero
if True:
    print("Se cumple la condición")
# Sino
else:
    print("No se cumple la condición")

# Verificar si un número es par
n = int(input("Ingrese un número entero: "))

if n % 2 == 0:
    print(f"El número {n} es par")
else:
    print(f"El número {n} es impar")
