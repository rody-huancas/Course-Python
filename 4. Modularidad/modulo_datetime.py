from datetime import datetime as d

# Obtener el tiempo actual
print(d.now())


dt = d.now()
# Mostrar solo el año
print(dt.year)


# Mostrar solo el mes
print(dt.month)


# Mostrar solo el día
print(dt.day)


# Mostrar solo el hora
print(dt.hour)


# Mostrar solo el minuto
print(dt.minute)


# Mostrar solo el segundos
print(dt.second)
